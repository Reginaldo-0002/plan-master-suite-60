
import { useState } from "react";
import { Button } from "@/components/ui/button";
import { Avatar, AvatarFallback, AvatarImage } from "@/components/ui/avatar";
import { Upload, Camera, Loader2 } from "lucide-react";
import { useToast } from "@/hooks/use-toast";
import { supabase } from "@/integrations/supabase/client";

interface AvatarUploadProps {
  currentAvatarUrl: string | null;
  onAvatarUpdate: (url: string) => void;
  userId: string;
  userName: string | null;
}

export const AvatarUpload = ({ currentAvatarUrl, onAvatarUpdate, userId, userName }: AvatarUploadProps) => {
  const [uploading, setUploading] = useState(false);
  const { toast } = useToast();

  const uploadAvatar = async (file: File) => {
    try {
      setUploading(true);
      console.log('üì∏ Starting avatar upload for user:', userId);

      // Validate file
      if (!file.type.startsWith('image/')) {
        throw new Error('Por favor, selecione apenas arquivos de imagem');
      }

      if (file.size > 5 * 1024 * 1024) {
        throw new Error('A imagem deve ter no m√°ximo 5MB');
      }

      // Create unique filename
      const fileExt = file.name.split('.').pop();
      const fileName = `${userId}-${Date.now()}.${fileExt}`;
      const filePath = `${userId}/${fileName}`;

      // Remove old avatar if exists
      if (currentAvatarUrl && currentAvatarUrl.includes('supabase.co')) {
        try {
          const oldPath = currentAvatarUrl.split('/').slice(-2).join('/');
          console.log('üóëÔ∏è Removing old avatar:', oldPath);
          await supabase.storage
            .from('avatars')
            .remove([oldPath]);
        } catch (error) {
          console.log('‚ö†Ô∏è Could not remove old avatar:', error);
        }
      }

      // Upload to Supabase Storage
      console.log('‚òÅÔ∏è Uploading to path:', filePath);
      const { error: uploadError } = await supabase.storage
        .from('avatars')
        .upload(filePath, file, {
          cacheControl: '3600',
          upsert: true
        });

      if (uploadError) {
        console.error('‚ùå Upload error:', uploadError);
        throw new Error(`Erro no upload: ${uploadError.message}`);
      }

      // Get public URL
      const { data: { publicUrl } } = supabase.storage
        .from('avatars')
        .getPublicUrl(filePath);

      if (!publicUrl) {
        throw new Error('Erro ao obter URL da imagem');
      }

      console.log('üîó New avatar URL:', publicUrl);

      // Update profile in database
      console.log('üíæ Updating profile in database...');
      const { data: updatedProfile, error: updateError } = await supabase
        .from('profiles')
        .update({ 
          avatar_url: publicUrl,
          updated_at: new Date().toISOString()
        })
        .eq('user_id', userId)
        .select()
        .single();

      if (updateError) {
        console.error('‚ùå Database update error:', updateError);
        throw new Error(`Erro ao atualizar perfil: ${updateError.message}`);
      }

      console.log('‚úÖ Profile updated in database:', updatedProfile);

      // Force immediate UI update
      onAvatarUpdate(publicUrl);
      
      toast({
        title: "Sucesso",
        description: "Avatar atualizado com sucesso!",
      });

    } catch (error: any) {
      console.error('üí• Error uploading avatar:', error);
      toast({
        title: "Erro",
        description: error.message || "Erro ao fazer upload do avatar",
        variant: "destructive",
      });
    } finally {
      setUploading(false);
    }
  };

  const handleFileSelect = (event: React.ChangeEvent<HTMLInputElement>) => {
    const file = event.target.files?.[0];
    if (file) {
      uploadAvatar(file);
    }
  };

  return (
    <div className="flex flex-col items-center gap-4">
      <div className="relative">
        <Avatar className="h-24 w-24">
          <AvatarImage 
            src={currentAvatarUrl || ""} 
            key={`avatar-${currentAvatarUrl}-${Date.now()}`}
            onLoad={() => console.log('Avatar loaded:', currentAvatarUrl)}
            onError={() => console.log('Avatar load error:', currentAvatarUrl)}
          />
          <AvatarFallback className="text-lg">
            {userName ? userName.charAt(0).toUpperCase() : "U"}
          </AvatarFallback>
        </Avatar>
        
        <div className="absolute inset-0 flex items-center justify-center bg-black/50 opacity-0 hover:opacity-100 transition-opacity rounded-full cursor-pointer">
          <Camera className="w-6 h-6 text-white" />
        </div>
      </div>

      <div className="flex flex-col items-center gap-2">
        <input
          type="file"
          accept="image/*"
          onChange={handleFileSelect}
          className="hidden"
          id="avatar-upload"
          disabled={uploading}
        />
        <label htmlFor="avatar-upload">
          <Button
            variant="outline"
            disabled={uploading}
            className="cursor-pointer"
            asChild
          >
            <span>
              {uploading ? (
                <Loader2 className="w-4 h-4 mr-2 animate-spin" />
              ) : (
                <Upload className="w-4 h-4 mr-2" />
              )}
              {uploading ? "Enviando..." : "Alterar Avatar"}
            </span>
          </Button>
        </label>
        <p className="text-xs text-muted-foreground text-center">
          JPG, PNG at√© 5MB
        </p>
      </div>
    </div>
  );
};
